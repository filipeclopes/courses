numero_1 = int(input('Digite o primeiro numero: '))
numero_2 = int(input('Digite o segundo numero: '))
numero_real = float(input('Digite o numero Real: '))

resultado_1 = numero_1 * 2 * (numero_2 / 2)
print(f'o produto do dobro do primeiro com metade do segundo = {resultado_1}')

resultado_2 = numero_1 * 3 + numero_real
print(f'a soma do triplo do primeiro com o terceiro = {resultado_2}')

resultado_3 = numero_real ** 3
print(f'o terceiro elevado ao cubo = {resultado_3}')
